  use mod_control, only : psp_type
  use mod_rad, only : nrad, xrad
  use mod_bas, only : znuc, pp_vlocHF
  implicit none
  !--------------------------------------------------------------------
  !--------------------------------------------------------------------
  integer(c_int) :: ipar, irad, label
  real(c_double) :: Par(1:8,1:2,0:2), V(0:2)

  label = znuc
  if (psp_type == 2) then
     call TrailNeeds_getParHF(label,Par)
  else if (psp_type == 3) then
     call TrailNeeds_getParDF(label,Par)
  else
     pp_vlocHF = 0d0
     return
  end if
  !debug
  !do ipar = 1, 8
  !   write(6,"('TrailNeeds, Vd: ',2f20.10)") Par(ipar,2,2),Par(ipar,1,2)
  !end do
  !do ipar = 1, 8
  !   write(6,"('TrailNeeds, Vs-Vd: ',2f20.10)") Par(ipar,2,0),Par(ipar,1,0)
  !end do
  !do ipar = 1, 8
  !   write(6,"('TrailNeeds, Vp-Vd: ',2f20.10)") Par(ipar,2,1),Par(ipar,1,1)
  !end do
  !debug

  do irad = 1, nrad - 1
     call TrailNeeds_getV(xrad(irad), V)
     pp_vlocHF(irad,0) = V(0)+V(2)
     pp_vlocHF(irad,1) = V(1)+V(2)
     pp_vlocHF(irad,2) = V(2)
  end do

!debug
  do irad = 1, nrad - 1
     write(6,"('vloc: ',4f20.10)") xrad(irad),pp_vlocHF(irad,0:2)
  end do
!debug
!  stop 'for debug @ bas_pp_trailneeds'

  contains
  subroutine TrailNeeds_getV(rad,V)
    implicit none
    !--------------------------------------------------------------------
    real(kind(0d0)),value :: rad
    real(kind(0d0)),intent(out) :: V(0:2)
    !--------------------------------------------------------------------
    real(kind(0d0)) :: tay
    V(0) = Par(1,1,0)       *exp(-Par(1,2,0)*rad**2) &
         + Par(2,1,0)       *exp(-Par(2,2,0)*rad**2) &
         + Par(3,1,0)*rad   *exp(-Par(3,2,0)*rad**2) &
         + Par(4,1,0)*rad   *exp(-Par(4,2,0)*rad**2) &
         + Par(5,1,0)*rad   *exp(-Par(5,2,0)*rad**2) &
         + Par(6,1,0)*rad**2*exp(-Par(6,2,0)*rad**2) &
         + Par(7,1,0)*rad**2*exp(-Par(7,2,0)*rad**2) &
         + Par(8,1,0)*rad**2*exp(-Par(8,2,0)*rad**2)
    V(1) = Par(1,1,1)       *exp(-Par(1,2,1)*rad**2) &
         + Par(2,1,1)       *exp(-Par(2,2,1)*rad**2) &
         + Par(3,1,1)*rad   *exp(-Par(3,2,1)*rad**2) &
         + Par(4,1,1)*rad   *exp(-Par(4,2,1)*rad**2) &
         + Par(5,1,1)*rad   *exp(-Par(5,2,1)*rad**2) &
         + Par(6,1,1)*rad**2*exp(-Par(6,2,1)*rad**2) &
         + Par(7,1,1)*rad**2*exp(-Par(7,2,1)*rad**2) &
         + Par(8,1,1)*rad**2*exp(-Par(8,2,1)*rad**2)
    if (rad > 1D-2) then
       V(2) = -Par(1,1,2)/rad*(1d0-exp(-Par(1,2,2)*rad**2)) &
            + Par(2,1,2)*exp(-Par(2,2,2)*rad**2) &
            + Par(3,1,2)*exp(-Par(3,2,2)*rad**2) &
            + Par(4,1,2)*rad*exp(-Par(4,2,2)*rad**2) &
            + Par(5,1,2)*rad*exp(-Par(5,2,2)*rad**2) &
            + Par(6,1,2)*rad**2*exp(-Par(6,2,2)*rad**2) &
            + Par(7,1,2)*rad**2*exp(-Par(7,2,2)*rad**2) &
            + Par(8,1,2)*rad**2*exp(-Par(8,2,2)*rad**2)
    else
       tay = Par(1,2,2)
       tay = tay*rad - 0.5d0*tay**2*rad**3 + tay**3*rad**5/6d0 - tay**4*rad**7/24d0
       V(2) = -Par(1,1,2)*tay &
            + Par(2,1,2)*exp(-Par(2,2,2)*rad**2) &
            + Par(3,1,2)*exp(-Par(3,2,2)*rad**2) &
            + Par(4,1,2)*rad*exp(-Par(4,2,2)*rad**2) &
            + Par(5,1,2)*rad*exp(-Par(5,2,2)*rad**2) &
            + Par(6,1,2)*rad**2*exp(-Par(6,2,2)*rad**2) &
            + Par(7,1,2)*rad**2*exp(-Par(7,2,2)*rad**2) &
            + Par(8,1,2)*rad**2*exp(-Par(8,2,2)*rad**2)
    end if
  end subroutine TrailNeeds_getV
